package day04_concatenation;

public class ArithmeticOperators {

    public static void main (String[] args){

        System.out.println(100 + 200); // 300

        System.out.println(40 - 10); // 30

        System.out.println(50 / 6); //int value 8

        System.out.println(50 / 6.0);

        System.out.println(50.0 / 6);

        System.out.println(50d / 6); //d for double value

        System.out.println( (int) (2.5 / 0.5) ); // explicit casting


        // System.out.println( 9 / 0 ); - must not be divided by '0'


        System.out.println( 20 * 12 );


        System.out.println(100 % 13); // returns 'ostatok'

        System.out.println(45 % 8);

    }
}
